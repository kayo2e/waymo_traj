if __name__ == "__main__":
    # 1. 지정된 경로에서 npz 데이터를 불러오는 커스텀 Dataset 정의
    class WaymoCacheDataset(Dataset):
        def __init__(self, cache_dir):
            self.cache_dir = cache_dir
            self.cumulative_sizes = []

            if os.path.exists(cache_dir):
                # 해당 폴더 내의 .npz 파일들만 리스트업 (재현성을 위해 정렬)
                self.file_list = sorted(
                    f for f in os.listdir(cache_dir)
                    if f.endswith('.npz') and os.path.isfile(os.path.join(cache_dir, f))
                )
                print(f"[{cache_dir}] 경로에서 {len(self.file_list)}개의 .npz 데이터 파일을 찾았습니다.")
                total_scenarios = 0
                for f in self.file_list:
                    # mmap_mode='r'을 사용하여 파일을 메모리에 전부 올리지 않고 메타데이터(shape)만 빠르게 읽습니다.
                    # with 블록으로 핸들을 즉시 해제하여 자원 누수 방지.
                    with np.load(os.path.join(cache_dir, f), mmap_mode='r') as data:
                        num_scenarios = data["agents"].shape[0] # 해당 파일 내의 시나리오(배치) 개수
                    total_scenarios += num_scenarios
                    self.cumulative_sizes.append(total_scenarios)
            else:
                print(f"경고: {cache_dir} 경로를 찾을 수 없습니다. 폴더가 존재하는지 확인해주세요.")
                self.file_list = []

        def __len__(self):
            if not self.cumulative_sizes:
                return 0
            return self.cumulative_sizes[-1]

        def __getitem__(self, idx):
            if not self.file_list:
                raise IndexError("데이터셋이 비어있습니다.")

            # 1. 요청된 전역 idx가 어느 파일에 속하는지 이진 탐색(bisect)으로 빠르게 찾기
            file_idx = bisect.bisect_right(self.cumulative_sizes, idx)

            # 2. 해당 파일 내에서의 로컬 인덱스(local_idx) 계산
            if file_idx == 0:
                local_idx = idx
            else:
                local_idx = idx - self.cumulative_sizes[file_idx - 1]

            file_path = os.path.join(self.cache_dir, self.file_list[file_idx])

            # 3. npz 파일 로드
            data = np.load(file_path, allow_pickle=True)

            try:
                # 4. 파일에서 [local_idx]에 해당하는 '단일 시나리오'만 추출
                agent = data['agents'][local_idx]
                scene = data['scenes'][local_idx]
                traf = data['trafs'][local_idx]
                gt_trajs = data['gt_trajs'][local_idx]
                gt_valids = data['gt_valids'][local_idx]
                risk_label = data['risk_labels'][local_idx]
            except KeyError:
                print(f"\n[오류] {file_path} 파일에 지정한 키가 없습니다. 존재하는 키: {data.files}")
                raise

            # 5. numpy 배열을 PyTorch Tensor로 변환 및 차원(Shape) 맞추기
            # 주의: 모델 입력 shape에 표시된 선두 차원 '1'은 배치 차원이며 DataLoader가 자동으로 추가한다.
            # 따라서 Dataset은 한 샘플 분량만 반환해야 하므로 .unsqueeze(0)는 사용하지 않는다.
            # map_scene은 데이터의 마지막 피처 8개 중 모델이 요구하는 앞 6개만 슬라이싱.
            inputs = {
                "ego_hist": torch.tensor(agent[0], dtype=torch.float32),       # [11, 10] -> 배치 후 [B, 11, 10]
                "social": torch.tensor(agent[1:], dtype=torch.float32),         # [31, 11, 10] -> [B, 31, 11, 10]
                "map_scene": torch.tensor(scene[..., :6], dtype=torch.float32), # [50, 10, 6] -> [B, 50, 10, 6]
                "traf_t": torch.tensor(traf, dtype=torch.float32),              # [6, 1] -> [B, 6, 1]
                "risk_label": torch.tensor(risk_label, dtype=torch.float32)     # [3] -> [B, 3]
            }

            targets = {
                "gt_trajs": torch.tensor(gt_trajs, dtype=torch.float32),
                "gt_valids": torch.tensor(gt_valids, dtype=torch.float32),
            }

            return inputs, targets


    # 3. 데이터 로더 생성 (요청하신 Waymo Trajectory 캐시 경로 반영)
    train_dir = "/home/dtlab/gy/waymo_traj/waymo_traj/cache/train"
    val_dir = "/home/dtlab/gy/waymo_traj/waymo_traj/cache/val"

    train_dataset = WaymoCacheDataset(train_dir)
    val_dataset = WaymoCacheDataset(val_dir)

    # 궤적(Trajectory) 데이터처럼 순서나 셔플이 중요한 경우 옵션을 조정하세요.
    train_loader = DataLoader(train_dataset, batch_size=16, shuffle=False)
    val_loader = DataLoader(val_dataset, batch_size=16, shuffle=False)

    # 4. 모델 인스턴스화 및 진단 함수 호출
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    model = RiskConditionedModel(d_model=128, K=6, n_layers=2,
                                     map_dim=8,
                                     use_lane_mamba=True,
                                     use_risk_prefix=True,
                                     use_traj_fix=True,
                                     use_map_per_step=False,
                                     use_ar=False,
                                     use_lane_anchor=False,
                                     cond_dim=3).to(device)

    # 오버피팅 판단 격차 1.0m (minADE 단위; K=6 기준 0.5~1m 차이도 유의미)
    diagnose_model(model, train_loader, val_loader, device=device, overfit_gap=1.0)